-- Verification queries
SELECT 
    COUNT(*) as TotalRecords,
    MIN(SaleDate) as FirstSaleDate,
    MAX(SaleDate) as LastSaleDate,
    SUM(Total) as TotalSalesAmount
FROM [dbo].[Sales];

-- Summary by Province
SELECT 
    CASE 
        WHEN ProvinceId = 1 THEN 'Gauteng'
        WHEN ProvinceId = 2 THEN 'KwaZulu-Natal'
        WHEN ProvinceId = 5 THEN 'Limpopo'
        WHEN ProvinceId = 6 THEN 'Mpumalanga'
        WHEN ProvinceId = 8 THEN 'Free State'
        ELSE 'Private Customer'
    END as Province,
    COUNT(*) as SalesCount,
    SUM(Total) as TotalSales
FROM [dbo].[Sales]
WHERE SaleDate >= '2025-09-01' AND SaleDate <= '2025-09-30'
GROUP BY ProvinceId
ORDER BY TotalSales DESC;

